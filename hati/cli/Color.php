<?php

namespace hati\cli;

/**
 * A helper class consisting color constants for CLI output.
 *
 * @since 5.0.0
 * */

abstract class Color {

	/*
	 * Support CLI foreground color
	 * */
	const FG_BLACK        = '0;30';
	const FG_DARK_GRAY    = '1;30';
	const FG_BLUE         = '0;34';
	const FG_DARK_BLUE    = '0;34';
	const FG_LIGHT_BLUE   = '1;34';
	const FG_GREEN        = '0;32';
	const FG_LIGHT_GREEN  = '1;32';
	const FG_CYAN         = '0;36';
	const FG_LIGHT_CYAN   = '1;36';
	const FG_RED          = '0;31';
	const FG_LIGHT_RED    = '1;31';
	const FG_PURPLE       = '0;35';
	const FG_LIGHT_PURPLE = '1;35';
	const FG_YELLOW       = '0;33';
	const FG_LIGHT_YELLOW = '1;33';
	const FG_LIGHT_GRAY   = '0;37';
	const FG_WHITE        = '1;37';

	/*
	 * Supported CLI background color
	 * */
	const BG_BLACK      = '40';
	const BG_RED        = '41';
	const BG_GREEN      = '42';
	const BG_YELLOW     = '43';
	const BG_BLUE       = '44';
	const BG_MAGENTA    = '45';
	const BG_CYAN       = '46';
	const BG_LIGHT_GRAY = '47';

}